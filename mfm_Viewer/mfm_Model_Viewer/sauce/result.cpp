//=============================================================================
// 初期化処理
//=============================================================================
void ResultScene::Init()
{
	InitResultUI();
}

//=============================================================================
// 終了処理
//=============================================================================
void ResultScene::UnInit()
{
	UninitResultUI();
}

//=============================================================================
// 更新処理
//=============================================================================
void ResultScene::Updata()
{
	UpdateResultUI();
	UpdateResultCamera();
	if (IsFadeIN())
	{
		ChangeScene(SCENE_TITLE);
		Fade(FADE_OUT, D3DCOLOR_RGBA(0, 0, 0, 0));
	}
	if (IsFadeOUT())
	{
		SetKeyboardActive(KEYBOARDON);
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void ResultScene::Draw()
{
	DrawResultUI();
}
