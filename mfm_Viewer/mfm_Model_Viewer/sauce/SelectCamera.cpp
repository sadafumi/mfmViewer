//=============================================================================
// 更新処理
//=============================================================================
void UpdateSelectCamera(void)
{

	static D3DXVECTOR3 eye = D3DXVECTOR3(0.0f, 0.0f, -1.0f);
	//注視点
	static D3DXVECTOR3 at = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	//カメラの向き
	static D3DXVECTOR3 up = D3DXVECTOR3(0.0f, 1.0f, 0.0f);
	static float i = 0;
	SelectCame.SetCameraUp(up);
	SelectCame.SetEyePos(eye);
	SelectCame.SetLookat(at);
	SelectCame.Updata();
	i += 0.05f;
	if (i >= 360)
	{
		i = 0;
	}
}

